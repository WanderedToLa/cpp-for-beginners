#include<bits/stdc++.h> 
//<bits/stdc++.h> 헤더파일을 포함하라는 전처리기를 의미
// 혹은 선행처리기 , preprocessor 라고함.
using namespace std;

// Data Type을 의미 Data Type 변수명 과 같은 구조
// 변수명은 앞에 숫자가 올 수 없음.
int a;

int main(void)
{
    a = 10;
    cout << &a << '\n';
    return 0;
}

/*
* 변수(variable): 데이터를 저장하는 메모리 공간을 의미함.
* 
*/